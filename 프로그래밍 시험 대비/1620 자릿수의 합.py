  #문제만 보고 못 푼 문제들 중 하나
  #숫자 문자열이 들어왔을 때 한자리가 될 때까지 반복해서 한자리씩 더하는 문제
  #ex) 12345, 15, 6

n = int(input())  #계산할 숫자 문자열 입력
sum = 0  #한자리씩 더하는 변수
while sum == 0 or sum >= 10:  #더한 숫자 문자열이 0 이거나 두자리 이상인가?
  sum = 0  #더하는 숫자 문자열 초기화
  while n > 0:  #숫자 문자열이 0보다 큰가
    sum += int(n % 10)  #더하는 변수에 숫자 문자열의 마지막 자리 더함
    n = int(n / 10)  #숫자 문자열 마지막 자리 없앰
  n = sum  #최종적으로 더해진 변수를 숫자 문자열로 함

print(sum)